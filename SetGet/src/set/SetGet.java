package set;

public class SetGet {
             private double a;
             private double b;
             private double c;
             
             
             void seta(double d){
            	 a = d;
             }
             void setb(double e){
            	 b = e;
             }
             void setc(double f){
            	 c = f;
             }
             
             double geta(){
            	 return a;
             }
             double getb(){
            	 return b;
             }
             double getc(){
            	 return c;
             }
}
