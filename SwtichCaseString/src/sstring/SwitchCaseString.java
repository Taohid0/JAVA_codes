package sstring;

public class SwitchCaseString {
              public static void main (String args[])
              {
            	  String str = "one";
            	  
            	  switch(str)
            	  {
            	  case "one" : System.out.println("here printed one");
            	              break;
            	  case "two" : System.out.println("here printed two");
            	              break;
            	  }
              }
}
